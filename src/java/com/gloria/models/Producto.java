package com.gloria.models;

public class Producto {
    private int codPro;
    private String nomPro;
    private String tipoPro;
    private String desPro;
    private double prePro;
    private int stockPro;
    private String fecVenPro;
    private String tamPro;

    
    public int getCodPro() {
        return codPro;
    }

    public void setCodPro(int codPro) {
        this.codPro = codPro;
    }

    public String getNomPro() {
        return nomPro;
    }

    public void setNomPro(String nomPro) {
        this.nomPro = nomPro;
    }

    public String getTipoPro() {
        return tipoPro;
    }

    public void setTipoPro(String tipoPro) {
        this.tipoPro = tipoPro;
    }

    public String getDesPro() {
        return desPro;
    }

    public void setDesPro(String desPro) {
        this.desPro = desPro;
    }

    public double getPrePro() {
        return prePro;
    }

    public void setPrePro(double prePro) {
        this.prePro = prePro;
    }

    public int getStockPro() {
        return stockPro;
    }

    public void setStockPro(int stockPro) {
        this.stockPro = stockPro;
    }

    public String getFecVenPro() {
        return fecVenPro;
    }

    public void setFecVenPro(String fecVenPro) {
        this.fecVenPro = fecVenPro;
    }

    public String getTamPro() {
        return tamPro;
    }

    public void setTamPro(String tamPro) {
        this.tamPro = tamPro;
    }
}


